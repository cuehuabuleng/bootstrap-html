
function click_scroll() {
        var scroll_offset = $("#math").offset().top;
		var navheight=$("#nav").outerHeight();
		var oTop = $("#nav").offset().top;
		if(oTop>0){
			$("body,html").animate({
        scrollTop: scroll_offset-55
        }, 500);
		}else{
			$("body,html").animate({
        scrollTop: scroll_offset-76
        }, 500);
		}
        
    }
